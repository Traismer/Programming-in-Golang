/*
Ранее в рамках этого курса при решении задач требовалось прочитать что-то
со стандартного ввода и вывести результат соответственно в стандартный вывод.
При этом кто-то использовал пакет fmt, а кто-то - bufio + os. Все эти пакеты имеют
свои особенности, поэтому в этой задаче мы попробуем решить знакомую нам проблему
с помощью пакетов, которые кто-то мог до этого момента и не применять: bufio + os + strconv.

Задача состоит в следующем: на стандартный ввод подаются целые числа в диапазоне 0-100,
каждое число подается на стандартный ввод с новой строки (количество чисел не известно).
Требуется прочитать все эти числа и вывести в стандартный вывод их сумму.
*/

func main() {
	// Создаем новый сканер для чтения со стандартного ввода
	scanner := bufio.NewScanner(os.Stdin)

	// Переменная для хранения суммы
	sum := 0

	// Чтение каждой строки из ввода
	for scanner.Scan() {
		// Преобразование строки в целое число
		num, err := strconv.Atoi(scanner.Text())
		if err != nil {
			os.Stderr.WriteString("Ошибка преобразования: " + err.Error() + "\n")
			continue
		}

		// Добавление числа к сумме
		sum += num
	}

	// Проверка на ошибки во время сканирования
	if err := scanner.Err(); err != nil && err != io.EOF {
		os.Stderr.WriteString("Ошибка чтения: " + err.Error() + "\n")
	}

	// Используем bufio.Writer для вывода суммы в стандартный вывод
	writer := bufio.NewWriter(os.Stdout)
	writer.WriteString(strconv.Itoa(sum) + "\n")
	writer.Flush()
}