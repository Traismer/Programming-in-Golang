/*
Внутри функции main (объявлять ее не нужно) вы должны объявить функцию вида func(uint) uint,
которую внутри функции main в дальнейшем можно будет вызвать по имени fn.

Функция на вход получает целое положительное число (uint), возвращает число того же типа
в котором отсутствуют нечетные цифры и цифра 0. Если же получившееся число равно 0,
то возвращается число 100. Цифры в возвращаемом числе имеют тот же порядок, что и в исходном числе.
*/

fn := func(number uint) uint {
	// Преобразуем число в строку для обработки каждого символа
	numStr := strconv.FormatUint(uint64(number), 10)

	// Переменная для хранения результата
	var resultStr string

	// Проходим по каждой цифре в строке
	for _, char := range numStr {
		// Преобразуем символ в число
		digit := char - '0'
		// Проверяем, является ли цифра четной и не равной нулю
		if digit != 0 && digit%2 == 0 {
			resultStr += string(char)
		}
	}

	// Если результат пуст, возвращаем 100
	if resultStr == "" {
		return 100
	}

	// Преобразуем результат обратно в uint
	result, _ := strconv.ParseUint(resultStr, 10, 64)
	return uint(result)
}